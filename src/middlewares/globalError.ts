import { NextFunction, Request, Response } from "express";
import AppError from "../errors/AppError";
import { handleDuplicateError } from "../errors/duplicate.errors";
import { handleCastError } from "../errors/cast.error";
import { handleZodError } from "../errors/zod.errors";
import { handleValidationError } from "../errors/validation.errors";
import { ENV } from "../config/env.config";

export const globalErrorHandler = async (
  err: any,
  req: Request,
  res: Response,
  next: NextFunction
) => {
  let statusCode = 500;
  let message = "Something went wrong!";
  let errorSources: any = [];

  if (ENV.NODE_ENV === "development") {
    console.log(err);
  }

  //duplicatet error
  if (err.code === 11000) {
    const simplifiedError = handleDuplicateError(err);
    statusCode = simplifiedError.statusCode;
    message = simplifiedError.message;
    //objectId error /cast error
  } else if (err.name === "CastError") {
    const simplifiedError = handleCastError(err);
    statusCode = simplifiedError.statusCode;
    message = simplifiedError.message;
    //zod error
  } else if (err.name === "ZodError") {
    const simplifiedError = handleZodError(err);
    statusCode = simplifiedError.statusCode;
    message = simplifiedError.message;
    errorSources = simplifiedError.errorSources;
  }
  //Mongoose Validation Error
  else if (err.name === "ValidationError") {
    const simplifiedError = handleValidationError(err);
    statusCode = simplifiedError.statusCode;
    message = simplifiedError.message;
    errorSources = simplifiedError.errorSources;
  } else if (err instanceof AppError) {
    statusCode = err.statusCode;
    message = err.message;
  } else if (err instanceof Error) {
    statusCode = 500;
    message = err.message;
  }
  res.status(statusCode).json({
    success: false,
    message: message,
    errorSources,
    err: ENV.NODE_ENV === "development" ? err : null,
    stack: ENV.NODE_ENV === "development" ? err.stack : null,
  });
};
